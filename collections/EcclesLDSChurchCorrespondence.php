A collection of personal correspondence to David Eccles from the First Presidency and various other offices of the Church of Jesus Christ of Latter-day Saints. The papers range in date from 1886-1912. The papers contain correspondence from Joseph F. Smith, the Church European Printings, Publishing & Emigration Office, Weber Stake of Zion, and LDS Church Bonds.</p>

