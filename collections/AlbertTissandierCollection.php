</p>
Images from Albert Tissandier's trips to North America during 1885 and 1886. Tissandier was born in France in 1839 and died in 1906. A French visitor to the American West in the 1880s, he left a series of pencil drawings of still-recognizable locales in and near Utah. The Utah Museum of Fine Arts holds what may be the most complete collection of these appealing works.</p>

