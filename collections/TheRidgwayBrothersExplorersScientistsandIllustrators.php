</p><br/>This digital collection highlights the art work, correspondence, and research of the Ridgway brothers, John and Robert, late nineteenth century naturalists and illustrators of the American West. In 1864, 14 year old Robert began a correspondence with noted naturalist Spencer Baird. This relationship led to Robert being asked to join geologist Clarence King as the zoologist of the 1867-69 United States Geological Fortieth Parallel Survey. Ridgway joined the expedition in San Francisco after traveling by steamboat through the Caribbean and the Isthmus of Panama. He accompanied the expedition through the Sierra Nevada Mountains, over the Great Basin desert and ended up in Utah . He left the expedition in fall of 1868 and returned the next summer to collect specimens around the Great Salt Lake and Wasatch Mountains.</p><br/>
