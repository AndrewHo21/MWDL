</p>
This collection contains images of businesses and homes in the Ogden area. The photographs were taken by the Weber County Assessor's Office for tax purposes. The photographs range in date from the 1920s to the 1970s. The photographs are organized by city and then plot number leading to an address. Not all streets or buildings are included.</p>

