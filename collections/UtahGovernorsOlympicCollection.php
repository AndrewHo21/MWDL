</p>
Five talented photojournalists shot thousands of photographs documenting the Salt Lake 2002 Olympic Winter Games. The photographs capture the Games from the vantage point of the Utah Governor's Office and the activities of Governor Michael Leavitt and Lt. Governor Olene Walker. The Governor and Lt. Governor met with President George W. Bush, members of his cabinet, representatives and athletes from 77 countries, guests of state, and the people of Utah. </p>

