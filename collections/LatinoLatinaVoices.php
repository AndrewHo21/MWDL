</p>
Cache Valley, Utah is home to a rich and varied Latino population whose voice is often underrepresented in local repositories. To rectify this, in the summer/fall 2007 Utah State University's Special Collections and Archives collected the voices of 45 local Latino/a residents which are now presented in this digital collection. The interviews feature people originally from Mexico, Guatemala, Brazil, California and yes, even Utah. The interviewees talk about family traditions, religion, jobs and interests, as well as challenges associated with relocation, cultural identity and cultural differences.</p>

