</p>
Brand books contain all livestock brands and ear marks registered with the State Department of Agriculture. Livestock is assumed to include all types of animals raised for pleasure or for profit. The 1917 law was the first to specifically mention cattle, horses, and mules as livestock [ Laws of Utah, chapter 105 (1917)], although the brands of these and perhaps other animals may have been recorded before then. Subsequent revisions of the brand law expanded the definition of livestock to include several other animals. </p>
