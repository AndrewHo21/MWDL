<p></p>
<p>In 1928, Utah Construction Company completed its first project outside of the United States with the 110 mile railroad for Southern Pacific of Mexico. Over the next 30 years, UCC continued to work on projects in Mexico including dams, roads, mining, and canals. The collection contains several booklets and correspondence along with approximately 500 photographs.</p>
