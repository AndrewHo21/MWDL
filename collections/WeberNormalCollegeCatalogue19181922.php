<p></p>
<p>This collection consists of the annual catalogs issued by Weber through six different name changes. The first catalog was issued in 1897 for the Weber Stake Academy when the academy was affiliated with the Church of Jesus Christ of Latter-day Saints. In 1908 the school became known as the Weber Academy, and changing its name again to Weber Normal College in 1918. By 1922 the institution had changed its name to Weber College and in 1935; the church transferred the institution to the State of Utah. As the school continued to evolve, it became known as Weber State College in 1963, and finally in 1991 it gained university status and became known as Weber State University. The catalogs in this collection provide a rich history of the evolution of courses being taught throughout the progression of the institution. </p>
