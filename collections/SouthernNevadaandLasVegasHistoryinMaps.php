</p>
The historic map collection housed in the UNLV Libraries' Division of Special Collection dates from the 17th century to the present, including significant collections concerning the exploration of the American West, the American Southwest, Nevada, and Southern Nevada. This collection contains the original published reports of the great 19th century U.S. government surveys of the West, as well as the series of maps and atlases by Fremont, Emory, Beckwith, Warren and Wheeler that accompanied the published accounts.</p>

