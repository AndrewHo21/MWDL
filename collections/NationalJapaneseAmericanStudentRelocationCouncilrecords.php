University of Oregon Libraries,The National Japanese American Student Relocation Council was created by university administrators as a means of relocating Japanese American college students to other universities and colleges away from the West coast during World War II, and to prevent these students from being interned in government-run internment camps. At the University of Oregon, Karl Onthank, Dean of Personnel Administration, represented the University in relocating UO Japanese American students. The collection includes correspondence, newsletters, speeches, minutes of meetings, and ephemera.
