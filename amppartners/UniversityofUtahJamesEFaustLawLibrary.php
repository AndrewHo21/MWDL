<br/><p>The S.J. Quinney College of Law was founded in 1913 to meet the growing legal needs of Utah and the western United States. As one of the oldest and most well-respected law schools in the west, the S.J. Quinney College of Law has left a lasting mark on the American legal system. The James E. Faust Law Library is dedicated to the teaching, research, and service mission of the College of Law. As the legal information repository for the University of Utah and the largest public law library in the state, the library also serves the legal information needs of the broader community. The library houses law and law-related material and serves as a selective depository for both US government and Utah documents. </p><br/>
