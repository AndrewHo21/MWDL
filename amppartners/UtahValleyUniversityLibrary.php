Utah Valley University Library
<!-- Article Text -->

<p>Utah Valley University, located in Orem, Utah, has evolved from its beginnings as the Central Utah Vocational School in 1941, with 317 students, to a university with over 32,000 students. In addition to 57 baccalaureate programs, UVU now offers master degrees in education, nursing, and business administration, and still remains committed to career and technical programs. UVU's emphasis on teaching and engaged learning led to its classification as "community engaged" by the Carnegie Foundation.</p>
<p>The 200,000 square foot Utah Valley University Library opened in July of 2008, becoming Utah's "greenest" state-owned building. Along with state-of-the-art research resources and technology, the Library is also home to the George Sutherland Archives, which collects materials dealing with the history and scholarly output of UVU, as well as those which document the history of the surrounding area and communities. Most of the digitized materials in the Utah Valley University Library collection of the Mountain West Digital Library are from the George Sutherland Archives. </p>



