<p>Construction of the new North Logan City Library was started in August of 1998 and completed in October of 1999. The 11,200 square foot building is angled to face the majestic east bench mountains of Cache Valley on 2500 North, a main thoroughfare entering North Logan City. Vaulted ceilings, which soar to 30-foot peaks, are lined with rustic pine boards and compliment a large array of lush live foliage below. The southeast exterior wall boasts expansive windows from floor to ceiling offering patrons a dramatic view of the Cache mountains. This feature, along with sumptuous seating, creates a most soothing backdrop for the ever-growing collection of over 60,000 books and materials..</p>



