<p>In 1964 A. R. Mortensen and C. Gregory Crampton, two University of Utah history professors, founded the American West Center with the then unique mission of researching the history and culture of the American West. Over the last forty-three years the Center has accomplished far more than Mortensen and Crampton could have imagined. Faculty and graduate student researchers affiliated with the Center have taken over 7,000 oral histories, including an unparalleled 2,000 Native American interviews. Center staff have also created a remarkable archive of documents essential to the study of the Western environment and produced myriad curriculum support materials--most notably seventeen community history textbooks--for underserved communities across the region.</p>

