<p>
<p>The Division of Administrative Rules is an executive branch agency under the Department of Administrative Services.</p>
<p>Administrative rulemaking was first centralized in Utah in 1973. This action followed a national trend that began in 1935 with the passage of the Federal Register Act, and in 1946 with the passage of the federal Administrative Procedure Act. Prior to 1973, individual agencies in Utah used their own processes for issuing rules. In 1973, the Legislature passed the Utah Administrative Rule Making (sic) Act (Chapter 172, Laws of Utah 1973) and charged the State Archivist with the responsibility for receiving rule filings and publishing rules.</p>
<p>In 1985, to heighten awareness of administrative rules and to address what appeared to be a lack of attention by the executive branch with regard to the regulatory process, the Legislature assigned the rulemaking functions to the Office of Administrative Rules, with a State Rulemaking Coordinator answering to the State Archivist as part of a recodification of the entire process (Chapter 158, Laws of Utah 1985). In 1987, to further emphasize the importance of rulemaking and to secure funding for publication, the Legislature changed the 'Office' designation to 'Division'. The director reports to the Executive Director of the Department of Administrative Services (a cabinet department) (Chapter 241, Laws of Utah 1987).</p>


