<p>The Morgan County Library serves the community of Morgan, UT. </p>
<p>The Morgan County Historical Society was established in 1979 and is a department of Morgan County Government. Our office and repository are located in the Centennial Library. The goal of the Society is to preserve the historical record of Morgan County and its people; understanding that the history of our county and its people gives us a better appreciation of our state and national heritage. The primary function of the Society is to research, discover, collect and assemble any materials or information which may help establish, interpret and illustrate the history of Morgan County. The Society collects and preserves biographies, journals, diaries, histories, photographs and germane materials in its archives. All of these materials are available to the public.</p>


