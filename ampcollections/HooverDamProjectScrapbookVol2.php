This scrapbook, donated by H. J. Lawler, documents through photographs the construction of the dam from 1931 to 1935, showcasing UCC's part in its creation. In 1931, Utah joined Six Companies Inc. to raise the surety required to bid for the Boulder (Hoover) Dam contract. One of the world's largest dams, Hoover Dam was completed in 1936 at a cost of $49 million.</p>
