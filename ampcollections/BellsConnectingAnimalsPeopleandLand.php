Animal belling traditions began in Africa more than 5,000 years ago. Ever since then, bells have been used on various animals throughout the world from goats to chickens to sheep to camels. Bells: Connecting Animals, People and Land interprets how both handmade and industrial bells create relationships with cultures, landscapes and livestock.</p>

