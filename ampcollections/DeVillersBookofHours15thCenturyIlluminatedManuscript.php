</p>
This beautiful illuminated book of hours created in France during the late fifteenth century is a prayer book intended for use by lay persons to recite daily the Divine Office, a complicated series of prayers that changes daily. The manuscript includes family genealogy added by one of its many owners notes on the marriage, and on the birth and baptism of the children of Pierre de Villers and Jeanne de Chissiret, 1603-1621 in French.</p>

