</p>
Paul Laxalt was a longtime public figure in Nevada and beginning in the mid-1970s, he became a notable and highly visible player on the national political stage. Part of Laxalt’s appeal to both his colleagues and his constituents was his pragmatic and unapologetic approach to lawmaking. The son of Basque immigrants, Laxalt rose through the local political ranks before becoming the first Basque-American ever elected to the U.S. Senate and “the First Friend” of Ronald Reagan.</p>
The Ronald Reagan materials within the Paul Laxalt U.S. Senatorial Papers cover the years 1975 through 1987. They consist of a variety of personal papers, scrapbooks, audio/visual resources, and photographs from Reagan’s three presidential campaigns in which Laxalt served as the national chairman. Presented here is a small selection of these items that have been digitized and organized according to themes that reflect both Laxalt’s political career and his relationship with Ronald Reagan. </p>

