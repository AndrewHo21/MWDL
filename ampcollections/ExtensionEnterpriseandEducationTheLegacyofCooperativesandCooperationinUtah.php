Co-operatives are voluntary associations jointly-owned and operated for the benefit of those using their services. While the Co-operative Movement was a worldwide phenomena in the 1800s, it was particularly strong and pervasive early on in Utah. Nineteenth-century Utah pioneers embraced the spirit of cooperation, building shared irrigation systems and establishing co-op stores within their communities.</p>

