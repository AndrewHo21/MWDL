</p>
[Stacks], the Idaho Digital Repository, is a cooperative project of the Idaho Commission for Libraries and all state agencies. It is a searchable centralized collection of digital publications that are created by state agencies for the purpose of public information. </p>
 It was created by state statute 33-2505 which reads in part, "Recognizing that an informed citizenry is a cornerstone of effective democracy, and in order to provide free and continuous access to state publications, it shall be the duty of the state librarian to establish and maintain a publicly accessible repository of publications prepared by state agencies. The digital repository is intended to collect state publications and make them readily available to all Idaho citizens." </p>

