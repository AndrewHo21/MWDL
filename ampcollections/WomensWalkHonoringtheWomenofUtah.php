The Utah Women's Walk: Honoring the Women of Utah is a history project on a grand scale, the brainchild of UVU American Studies instructor Michele Welch. Mrs. Welch and many of her students have researched prominent women in Utah--both from history and those currently living--and have created biographies of them, conducted oral histories with those still living, and are planning a beautiful memorial garden to honor these women. This collection currently contains transcripts and audio of only several of the interviews, but this collection will grow soon with the addition of many more interviews, as well as video of the interviews.</p>
