</p>
 The Thomas Jefferson O'Brien journals were kept during his mission to Australasia for the Church of Jesus Christ of Latter-day Saints at the end of the nineteenth century. His journal relates his activities with the Maori on the Northern Island of New Zealand. The first volume covers the period from November 1893 to early February 1895. The second volume covers the period from February 1895 to January 1896.</p>

