</p>
This is a collection of scrapbooks compiled by the Utah Technical College Women's Association. This organization, called UTC Women for short, was organized in May 1969 to support faculty, staff and students at Utah Technical College. </p>
Membership consisted of the wives of UTC faculty and staff, as well as women serving on the faculty and staff of the college. The scrapbooks contain memorabilia of the various arts, humanities, and professional programs and socials held by the group, as well as that of their services to the community and the college.</p>

