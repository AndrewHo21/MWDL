</p>
A collection of 27 handwritten or typed correspondence letters written to or from various members of the Thomas and Annie Dee family. Some letters are from the secretary to President McKinley, various members of the First Presidency of the Church of Jesus Christ of Latter-Day Saints, and the spouses of the Dee children. Dates range from 1871 to 1959. </p>

