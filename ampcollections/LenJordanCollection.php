Len B. Jordan (Leonard Beck Jordan) served as Governor of Idaho from 1951 to 1955 and United States Senator from 1962 to 1973. This collection features photographic images and objects documenting highlights of Len Jordan?s political career as Idaho governor and a United States senator. The digital collection contains 75 images selected from the Len B. Jordan Papers (MSS 6) located at Boise State University in the Special Collections department.
