</p>
In 1996, Special Collections acquired from the Howard Hughes Corporation in Las Vegas the Hughes account and reference files of Dick Hannah, the Byoir Vice President and account executive who handled Howard Hughes's publicity. Like other Hughes agents, Hannah did far more than his title would imply. He not only directed public relations by drafting countless news releases and preparing mountains of press kits with sets of "authorized" photos, he gathered enormous amounts of information concerning what was written about Hughes or about subjects in which Hughes had interest.</p>

