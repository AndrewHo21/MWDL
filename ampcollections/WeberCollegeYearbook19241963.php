</p>
This is a collection of yearbooks 1924 to 1963. The yearbooks include photographs of students, class officers, faculty, Board of Education/Trustees, athletics, clubs, organizations and advertisements from local businesses. In 1924 the name changed to Weber College which includes the years 1924 to 1963. However, the yearbook publication ceased 1932-34; 1943-46 and the 1950-53 editions are Scribulus-Acorn publications, which are a condensed version of the yearbook.</p>

